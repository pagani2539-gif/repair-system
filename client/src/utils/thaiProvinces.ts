// อนุมานภูมิภาคจากชื่อจังหวัด — ใช้ลดฟิลด์ที่ผู้ใช้ต้องกรอกเอง
export const getRegionFromProvince = (province: string): string => {
  const trimmed = province.trim();
  if (!trimmed) return '';
  for (const [region, list] of Object.entries(provincesByRegion)) {
    if (list.some(p => p === trimmed)) return region;
  }
  return '';
};

// คืนรายชื่อจังหวัด — โดยจัดให้จังหวัดในภาคที่เลือกอยู่ก่อน
export const getProvincesForRegion = (region: string): string[] => {
  if (!region) return allThailandProvinces;
  const regionProvinces = provincesByRegion[region] || [];
  const others = allThailandProvinces.filter(p => !regionProvinces.includes(p));
  return [...regionProvinces, ...others];
};
